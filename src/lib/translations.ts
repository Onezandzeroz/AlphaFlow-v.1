// Helper function to get translation
export function t(key: keyof typeof translations, language: Language): string {
  return translations[key]?.[language] || key;
}

// Helper function to format currency in Danish/English style
export function formatCurrency(amount: number, language: Language): string {
  if (language === 'da') {
    return amount.toLocaleString('da-DK', {
      style: 'currency',
      currency: 'DKK',
    });
  }
  return amount.toLocaleString('en-DK', {
    style: 'currency',
    currency: 'DKK',
  });
}

// Helper function to format date in Danish/English style
export function formatDate(date: Date, language: Language): string {
  if (language === 'da') {
    return date.toLocaleDateString('da-DK', {
      day: 'numeric',
      month: 'short',
      year: 'numeric',
    });
  }
  return date.toLocaleDateString('en-GB', {
    day: 'numeric',
    month: 'short',
    year: 'numeric',
  });
}

// Helper function to format month/year
export function formatMonthYear(date: Date, language: Language): string {
  if (language === 'da') {
    return date.toLocaleDateString('da-DK', {
      month: 'long',
      year: 'numeric',
    });
  }
  return date.toLocaleDateString('en-GB', {
    month: 'long',
    year: 'numeric',
  });
}

